class Gallery extends Component {
  state = {imageTabId: imagesList[0].id}

  changeImage = id => {
    this.setState({
      imageTabId: id,
    })
  }
  render() {
    const {imageTabId} = this.state
    const {imageUrl, imageAltText} = imagesList[imageTabId]
    return (
      <div className="appContainer">
        <div className="galleryContainer">
          <img src={imageUrl} alt={imageAltText} className="largeImage" />

          <h1 className="">Nature Photography</h1>
          <p className="">Nature Photography by Rahul</p>
          <ul className="smallImagesContainer">
            {imagesList.map(eachImage => (
              <ThumbnailItem
                imageDetails={eachImage}
                key={eachImage.id}
                changeImage={this.changeImage}
                isActive={imageTabId === eachImage.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
